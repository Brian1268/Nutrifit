        <h4>Directions</h4>
        <ol>
          <li>Line a large rimmed baking sheet with foil; place in oven. Preheat the oven to 425Â°F.</li>
          <li>Meanwhile, remove any wilted or damaged leaves from cabbage. Cut the head into 8 (1 1/4-inch-thick) wedges, leaving the root end intact to help wedges stay together.</li>
          <li>Whisk oil, vinegar, lemon juice, mustard, garlic, pepper and 1/4 teaspoon salt together in a medium bowl. Reserve 2 tablespoons dressing in a small bowl.</li>
          <li>Divide the cabbage wedges between 2 (gallon-size) zip-top plastic bags; pour the remaining dressing from the medium bowl into the bags; rub to coat the cabbage with dressing. Let stand for 15 minutes, tossing the bags occasionally.</li>
          <li>Remove the baking sheet from the oven; arrange the marinated cabbage wedges on the pan in a single layer. Sprinkle with the remaining 1/4 teaspoon salt. Roast, flipping once, until tender and browned, 35 to 40 minutes.</li>
          <li>Drizzle the cabbage wedges with the reserved dressing and sprinkle with thyme.</li>
